# list is an ordered sequence of items, most used datatypes
list_of_numbers = [1,2,3,4,5]
print(type(list_of_numbers))

# dictionary is unordered collection of key value 
number_dict = {'name':'john', 'age':18, 'gender':'male'}

# tuple is sequence of items same as list 
tuple_of_numbers = (1,2,3,4,5,6)
print(type(tuple_of_numbers))

# a set is unordered collection of items
a_set_of_numbers = {1,2,3,4,5,6,7}
print(type(a_set_of_numbers))


